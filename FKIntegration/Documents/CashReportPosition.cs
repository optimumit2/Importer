namespace FKIntegration.Documents
{
    public class CashReportPosition
    {
        private readonly Document _document;
        private readonly int _number;

        public CashReportPosition(Document document, int number)
        {
            _document = document;
            _number = number;
        }

        private string _documentNumber;
        public string DocumentNumber
        {
            get { return _documentNumber; }
            set { _documentNumber = value; }
        }

        private string _description;
        public string Description
        {
            get { return _description; }
            set { _description = value; }
        }

        private string _oppositeAccountNumber;
        public string OppositeAccountNumber
        {
            get { return _oppositeAccountNumber; }
            set { _oppositeAccountNumber = value; }
        }
    }
}